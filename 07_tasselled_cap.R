bright.calc <- function(x) {
     (x[[1]] * 0.3037) + (x[[2]] * 0.2793) + (x[[3]] * 0.4343) + 
          (x[[4]] * 0.5585) + (x[[5]] + 0.5082) + (x[[6]] * 0.1863 )
}

green.calc <- function(x) {
     (x[[1]] * -0.2848) + (x[[2]] * -0.2435) + (x[[3]] * -0.536) + 
          (x[[4]] * 0.7243) + (x[[5]] + 0.0840) + (x[[6]] * -0.1800 )
}

wet.calc <- function(x) {
     (x[[1]] * 0.1509) + (x[[2]] * 0.1793) + (x[[3]] * 0.3299) + 
          (x[[4]] * 0.3406) + (x[[5]] + -0.7112) + (x[[6]] * -0.4572 )
}